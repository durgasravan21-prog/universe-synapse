/**
 * Verify if an email domain belongs to a known university
 */
export function verifyUniversityDomain(email: string): VerifiedUniversity | null {
  const domain = email.split('@')[1];
  if (!domain) return null;

  return VERIFIED_UNIVERSITIES.find(uni =>
    uni.officialDomains.some(officialDomain => domain.endsWith(officialDomain))
  ) || null;
}

/**
 * Check if a university name exists in the verified list
 */
export function findUniversityByName(name: string): VerifiedUniversity | null {
  return VERIFIED_UNIVERSITIES.find(uni =>
    uni.name.toLowerCase().includes(name.toLowerCase()) ||
    name.toLowerCase().includes(uni.name.toLowerCase())
  ) || null;
}

/**
 * Get all verified universities
 */
export function getAllVerifiedUniversities(): VerifiedUniversity[] {
  return VERIFIED_UNIVERSITIES;
}
